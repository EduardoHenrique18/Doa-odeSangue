    public String geraDataDoacao() {
        Calendar calendar = Calendar.getInstance();
        int ano = calendar.get(Calendar.YEAR);
        int mes = calendar.get(Calendar.MONTH);
        int dia = calendar.get(Calendar.DAY_OF_MONTH);
        String dia1 = Integer.toString(dia);
        String mes1 = Integer.toString(mes + 1);
        String ano1 = Integer.toString(ano);
        String data = dia1 + "/" + mes1 + "/" + ano1;
        return data;
    }

    public Boolean autorizarDoacao(String cpf_doador, String cpf_receptor) throws Exception {
        boolean log = false;
        String query = "select tipo_sangue from doador where cpf_doador = " + "'" + cpf_doador + "'";
        DoadorDAO dd = new DoadorDAO();
        ResultSet rsd = dd.conectar().executeQuery(query);
        rsd.next();
        dd.desconectar();
        //String sangue = rsd.getNString(1);
        String query1 = "select tipo_sangue from receptor where cpf_receptor = " + "'" + cpf_receptor + "'";
        ReceptorDAO rd = new ReceptorDAO();
        ResultSet rsr = rd.conectar().executeQuery(query1);
        rsr.next();
        rd.desconectar();
        if (rsd.getString("tipo_sangue").equals("A+") && rsr.getString("tipo_sangue").equals("A+") || rsr.getString("tipo_sangue").equals("AB+")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("A-") && rsr.getString("tipo_sangue").equals("A+") || rsr.getString("tipo_sangue").equals("A-") || rsr.getString(1).equals("AB+") || rsr.getString("tipo_sangue").equals("AB-")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("B+") && rsr.getString("tipo_sangue").equals("B+") || rsr.getString("tipo_sangue").equals("AB+")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("B-") && rsr.getString("tipo_sangue").equals("B+") || rsr.getString("tipo_sangue").equals("B-") || rsr.getString("tipo_sangue").equals("AB+") || rsr.getString("tipo_sangue").equals("AB-")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("AB+") && rsr.getString("tipo_sangue").equals("AB+")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("AB-") && rsr.getString("tipo_sangue").equals("AB+") || rsr.getString("tipo_sangue").equals("AB-")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("O+") && rsr.getString("tipo_sangue").equals("A+") || rsr.getString("tipo_sangue").equals("B+") || rsr.getString("tipo_sangue").equals("O+") || rsr.getString("tipo_sangue").equals("AB+")) {
            log = true;
        } else if (rsd.getString("tipo_sangue").equals("O-")) {
            log = true;
        }
        return log;
    }

