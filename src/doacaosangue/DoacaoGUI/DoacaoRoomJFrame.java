    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Doador dd = new Doador();
        DoadorNegocio ddn = new DoadorNegocio();
        dd.setCpf_doador(jTextField1.getText());
        Receptor r = new Receptor();
        ReceptorNegocio rn = new ReceptorNegocio();
        r.setCpf_receptor(jTextField2.getText());
        Medico m = new Medico();
        MedicoNegocio mn = new MedicoNegocio();
        m.setCrm(jTextField3.getText());
        Doacao d = new Doacao();
        d.setDt_doacao(this.geraDataDoacao());
        d.setDoador(dd);
        d.setMedico(m);
        d.setReceptor(r);
        DoacaoNegocio dn = new DoacaoNegocio();
        try {
            if (ddn.loginDoador(dd.getCpf_doador()) == true) {
                JOptionPane.showMessageDialog(null, "Doador não existente ou incorreto");
                jTextField1.grabFocus();
            } else if (rn.loginReceptor(r.getCpf_receptor()) == true) {
                JOptionPane.showMessageDialog(null, "Receptor não existente ou incorreto");
                jTextField2.grabFocus();
            } else if (mn.loginMedico(m.getCrm()) == true) {
                JOptionPane.showMessageDialog(null, "Medico não existente ou incorreto");
                jTextField3.grabFocus();
            } else {
                try {
                    if (autorizarDoacao(jTextField1.getText(), jTextField2.getText()) == true) {
                        dn.cadastrarDoacao(d);
                        JOptionPane.showMessageDialog(null, "Doou");
                    } else {
                        JOptionPane.showMessageDialog(null, "Tipos de sangue imcompativeis");
                    }
                } catch (Exception ex) {
                    Logger.getLogger(DoacaoRoomJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

