    private void carregarDadosTabela() {
        try {
            DoacaoNegocio negocio = new DoacaoNegocio();
            Doacao filtro = new Doacao();
            this.listaDoacao = negocio.listar(filtro);
            //vartiável para sobrepor o modelo antigo da tabela
            DefaultTableModel modelo = new DefaultTableModel();
            //definindo as colunas das tabela nova
            modelo.setColumnIdentifiers(new Object[]{"CD_Doacao", "Dt_Doacao", "Nome Medico", "Nome Doador", "Nome Receptor"});
            //percorrendo a coleção de cursos
            for (Doacao doacao : listaDoacao) {
                modelo.addRow(new Object[]{doacao.getCd_doacao(), doacao.getDt_doacao(), doacao.getMedico().getNome(), doacao.getDoador().getNome(), doacao.getReceptor().getNome()});
            }
            //sobrepondo o modelo antigo
            jTable2.setModel(modelo);
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

