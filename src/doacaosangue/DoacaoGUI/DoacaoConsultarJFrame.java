    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //verificando se algum curos foi escolhido
        int posicao = jTable2.getSelectedRow();
        if (posicao >= 0) {
            //separando o curso da lista de cursos
            Doacao doacaoEscolhida = this.listaDoacao.get(posicao);
            //enviando o curso escolhido, via construtor, para o form de alteracao
            DoacaoAlterarJFrame form = new DoacaoAlterarJFrame(doacaoEscolhida);
            form.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecionar uma doação");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       // TODO add your handling code here:
        //verificando se algum curos foi escolhido
        try {
            int posicao = jTable2.getSelectedRow();
            if (posicao >= 0) {
                int opcao = JOptionPane.showConfirmDialog(null, "Deseja remover realmente esta Doação?", "Atenção",
                    JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (opcao == 0) {
                    //separando o aluno da lista de alunos
                    Doacao doacaoEscolhida = this.listaDoacao.get(posicao);
                    DoacaoNegocio negocio = new DoacaoNegocio();
                    negocio.removerDoacao(doacaoEscolhida);
                    JOptionPane.showMessageDialog(rootPane, "Doação removida com sucesso");
                    this.carregarDadosTabela();
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Selecionar um Doacao");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

