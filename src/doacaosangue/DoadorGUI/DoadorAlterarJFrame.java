    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DoadorNegocio negocio = new DoadorNegocio();
            Doador D = new Doador();
            if (jTextField1.getText().trim().equals("") == false) {
                D.setCpf_doador(jTextField1.getText());
            }
            D.setNome(jTextField2.getText());
            D.setEndereco(jTextField3.getText());
            D.setSexo(jTextField4.getText());
            D.setTp_sangue(jTextField5.getText());
            negocio.atualizarDoador(D);

            JOptionPane.showMessageDialog(null, "Doador atualizado com sucesso");
            //fechando o form
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

