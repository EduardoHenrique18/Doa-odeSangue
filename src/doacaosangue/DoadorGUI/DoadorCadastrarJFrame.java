    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Doador d = new Doador();
            d.setCpf_doador(jTextField1.getText());
            d.setNome(jTextField2.getText());
            d.setEndereco(jTextField3.getText());
            if (jRadioButton1.isSelected() == true) {
                d.setSexo("M");
            } else {
                d.setSexo("F");
            }
            d.setTp_sangue((String) (jComboBox1.getSelectedItem()));
            DoadorNegocio dn = new DoadorNegocio();

            dn.cadastrarDoador(d);
            JOptionPane.showMessageDialog(null, "Cadastrou");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

